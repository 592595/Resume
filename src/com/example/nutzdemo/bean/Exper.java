package com.example.nutzdemo.bean;

public class Exper {
    private String username;
    private String Experid;
    private String ExperTitle;
    private String ExperContent;

    public String getusername() {
        return username;
    }
    public void setusername(String username) {
        this.username = username;
    }

    public String getExperid() {
        return Experid;
    }
    public void setExperid(String Experid) {
        this.Experid = Experid;
    }

    public String getExperTitle() {
        return ExperTitle;
    }
    public void setExperTitle(String ExperTitle) {
        this.ExperTitle = ExperTitle;
    }

    public String getExperContent() {
        return ExperContent;
    }
    public void setExperContent(String ExperContent) {
        this.ExperContent = ExperContent;
    }
}
